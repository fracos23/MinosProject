package it.unical.forms;


public class SubmitForm {

	private String path;
	private String team;
	
	public SubmitForm() {
		this.path = "";
		this.team = "";
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public String getTeam() {
		return team;
	}

	public void setTeam(String team) {
		this.team = team;
	}
}
	